package ex_27042024;

public class lab115 {
    public static void main(String[] args) {

        //while
        // A - Init
        // B - Condition
        // C - Incre
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++; // C
        }
        for (int j = 1; j <= 10; j++) {
            System.out.println(j);
        }
    }
}
