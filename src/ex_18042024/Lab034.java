package ex_18042024;

public class Lab034 {
    public static void main(String[] args){
// Logical Operator
        // Not Gate, OR Gate, And Gate

        boolean a = true;
        System.out.println(!a); //false
        System.out.println(!(10>20));//True
        System.out.println(!(30>90));//True
        System.out.println(!!(30>90));//false
        System.out.println(!!!(30>90));//True
        System.out.println(!!!!(30>90));//false
    }
}
